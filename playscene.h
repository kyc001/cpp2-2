// A* 算法所需的节点结构体
struct Node
{
    int x, y;
    int g; // 从起点到当前节点的实际代价
    int h; // 从当前节点到目标节点的预估代价
    int f; // f = g + h
    Node *parent;

    Node(int _x, int _y, int _g, int _h, Node *_parent = nullptr)
        : x(_x), y(_y), g(_g), h(_h), f(_g + _h), parent(_parent) {}

    bool operator>(const Node &other) const
    {
        return f > other.f;
    }
};

